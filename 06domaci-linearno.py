    A_ub = np.array(A_ub)
    A_eq = np.array(A_eq)
    b_ub = np.array(b_ub)
    b_eq = np.array(b_eq)

    result = scipy.optimize.linprog(c = c, method='highs', A_ub = A_ub, A_eq = A_eq, b_ub= b_ub, b_eq = b_eq)

